    private void TambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahActionPerformed
         try {
            // Ambil nilai dari TextField
            int angka1 = Integer.parseInt(txtangka1.getText());
            int angka2 = Integer.parseInt(txtangka2.getText());

            // Hitung jumlah
            int hasil = angka1 + angka2;

            // Tampilkan hasil
            Hasil1.setText(String.valueOf(hasil));
        } catch (NumberFormatException e) {
            // Tampilkan pesan error jika input belum di isi
            JOptionPane.showMessageDialog(this, "Input belum di isi!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_TambahActionPerformed

