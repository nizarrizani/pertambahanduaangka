    private void txtangka1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtangka1KeyTyped
        char c = evt.getKeyChar();
if (!Character.isDigit(c)) {
    evt.consume();  // Membatalkan input jika bukan angka
    JOptionPane.showMessageDialog(null, "Hanya bisa menginput angka!", "Error", JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_txtangka1KeyTyped

